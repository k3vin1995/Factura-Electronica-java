package model;

/**
 *
 * @author Usuario
 */
public class Moneda {
    
    
    private String dolar;
    private double valor;

    public Moneda() {
    }

    public Moneda( String dolar, double valor) {
        this.dolar = dolar;
        this.valor = valor;
    }

    public String getDolar() {
        return dolar;
    }

    public void setDolar(String dolar) {
        this.dolar = dolar;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    
    
}
